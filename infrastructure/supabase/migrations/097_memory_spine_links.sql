-- =============================================================================
-- TABLE: public.memory_event_inbox
-- =============================================================================
-- Append-only intake queue for MemoryEventEnvelope payloads. Events land here
-- first; the memory pipeline worker processes them into memory_objects and
-- proactive_candidates. 30-day TTL enforced via pg_cron (see cron setup below).
--
-- Status lifecycle: pending → processing → processed | dead_letter
-- Dead-lettered events are retained for forensics and replay.

CREATE TABLE IF NOT EXISTS public.memory_event_inbox (
    -- Primary key
    event_id        UUID    PRIMARY KEY DEFAULT gen_random_uuid(),

    -- Schema versioning
    schema_version  TEXT    NOT NULL DEFAULT 'v1',

    -- Tenant / suite / office scope (Law #6)
    tenant_id       UUID    NOT NULL,
    suite_id        UUID    NOT NULL,
    office_id       UUID    NOT NULL,

    -- Scope extensions (from ScopedIdentity)
    actor_id        UUID    NULL,
    user_id         UUID    NULL,

    -- Event classification
    event_type      TEXT    NOT NULL,

    -- Provenance (from MemoryEventEnvelope)
    source_surface  TEXT    NULL CHECK (source_surface IS NULL OR source_surface IN (
                                'ava_voice', 'sarah_voice', 'eli_inbox', 'nora_meeting',
                                'finn_finance', 'tim_service_lab', 'estimate_studio',
                                'canvas_desk', 'receipt_ledger', 'approval_queue', 'system'
                            )),
    source_agent    TEXT    NULL CHECK (source_agent IS NULL OR source_agent IN (
                                'ava', 'sarah', 'eli', 'nora', 'finn', 'tim', 'system'
                            )),
    runtime_family  TEXT    NULL CHECK (runtime_family IS NULL OR runtime_family IN (
                                'elevenlabs', 'anam', 'internal', 'ui', 'provider_webhook'
                            )),
    channel         TEXT    NULL CHECK (channel IS NULL OR channel IN (
                                'voice', 'video', 'email', 'sms', 'workflow',
                                'finance', 'ui', 'webhook'
                            )),
    trace_id        UUID    NOT NULL,
    correlation_id  UUID    NOT NULL,
    source_record_id TEXT   NULL,

    -- Thread / entity context
    session_id      UUID    NULL,
    thread_id       UUID    NULL,
    entity_type     TEXT    NULL,
    entity_id       UUID    NULL,

    -- Raw event payload (stored as-is; worker extracts domain objects)
    payload         JSONB   NOT NULL DEFAULT '{}'::jsonb,

    -- Governance fields from MemoryEventEnvelope
    risk_tier       TEXT    NOT NULL DEFAULT 'yellow'
                        CHECK (risk_tier IN ('green', 'yellow', 'red')),
    needs_approval  BOOLEAN NOT NULL DEFAULT false,
    receipt_required BOOLEAN NOT NULL DEFAULT false,

    -- Timestamps (from MemoryEventEnvelope)
    event_at            TIMESTAMPTZ NOT NULL,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    source_updated_at   TIMESTAMPTZ NULL,

    -- Idempotency: deduplicate re-delivered events within tenant+suite scope
    idempotency_key     TEXT    NOT NULL,
    CONSTRAINT uq_memory_event_inbox_idempotency
        UNIQUE (tenant_id, suite_id, idempotency_key),

    -- Queue processing state
    status          TEXT    NOT NULL DEFAULT 'pending'
                        CHECK (status IN ('pending', 'processing', 'processed', 'dead_letter')),
    attempts        INT     NOT NULL DEFAULT 0,
    last_error      TEXT    NULL,
    processed_at    TIMESTAMPTZ NULL,
    dead_lettered_at TIMESTAMPTZ NULL
);

COMMENT ON TABLE public.memory_event_inbox IS
    'Append-only intake queue for MemoryEventEnvelope payloads. '
    'Events land here; memory pipeline worker processes them into memory_objects. '
    '30-day TTL: rows with status=''processed'' and created_at < now()-30d are pruned by pg_cron. '
    'Dead-lettered rows are retained indefinitely for forensics and replay. '
    'RLS: tenant_id + suite_id + office_id scoped (Law #6).';

COMMENT ON COLUMN public.memory_event_inbox.idempotency_key IS
    'Caller-supplied dedup key. UNIQUE within (tenant_id, suite_id). '
    'Prevents duplicate processing of re-delivered events.';

-- Queue processing index (worker scans pending events ordered by arrival time)
CREATE INDEX IF NOT EXISTS idx_memory_event_inbox_queue
    ON public.memory_event_inbox (status, created_at ASC)
    WHERE status IN ('pending', 'processing');

-- Dead-letter review index
CREATE INDEX IF NOT EXISTS idx_memory_event_inbox_dead_letter
    ON public.memory_event_inbox (tenant_id, suite_id, dead_lettered_at DESC)
    WHERE status = 'dead_letter';

-- Tenant-scoped recency
CREATE INDEX IF NOT EXISTS idx_memory_event_inbox_tenant_activity
    ON public.memory_event_inbox (tenant_id, suite_id, created_at DESC);

