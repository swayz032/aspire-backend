-- =============================================================================
-- TABLE: public.receipt_memory_links
-- =============================================================================
-- Append-only join table between receipts (TEXT PK) and memory_objects (UUID PK).
-- No UPDATE, no DELETE — a link is immutable once created (Law #2).
-- The composite PK (receipt_id, memory_id) ensures no duplicate links.

CREATE TABLE IF NOT EXISTS public.receipt_memory_links (
    -- Composite primary key prevents duplicate links
    receipt_id      TEXT    NOT NULL
                        REFERENCES public.receipts(receipt_id)
                        ON DELETE CASCADE,
    memory_id       UUID    NOT NULL
                        REFERENCES public.memory_objects(memory_id)
                        ON DELETE CASCADE,

    -- How this link was established (e.g. 'direct', 'inferred', 'workflow')
    linked_via      TEXT    NULL,

    -- Tenant scope for RLS (denormalized from receipts)
    tenant_id       UUID    NOT NULL,
    suite_id        UUID    NOT NULL,

    created_at      TIMESTAMPTZ NOT NULL DEFAULT now(),

    PRIMARY KEY (receipt_id, memory_id)
);

COMMENT ON TABLE public.receipt_memory_links IS
    'Append-only join table between receipts and memory_objects. '
    'Immutable once created — no UPDATE or DELETE. '
    'Composite PK (receipt_id, memory_id) prevents duplicate links. '
    'RLS: tenant_id + suite_id scoped (Law #6).';

