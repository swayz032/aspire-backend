-- =============================================================================
-- Migration 097: Memory Spine — Link Tables and Event Inbox
-- =============================================================================
-- Creates four supporting tables for the Office Memory Engine:
--
--   1. public.proactive_candidates — surface-level action suggestions from agents
--   2. public.approval_links       — joins approval_requests to memory + candidates
--   3. public.receipt_memory_links — joins receipts to memory objects
--   4. public.memory_event_inbox   — append-only event intake queue (30-day TTL)
--
-- Also adds a TRIGGER on memory_objects (AFTER INSERT) to maintain the
-- threads.latest_memory_id and threads.last_activity_at denormalized caches.
--
-- Aspire Laws enforced:
--   Law #2 (Receipt for All)  — receipt_memory_links is append-only; no DELETE
--   Law #3 (Fail Closed)      — RLS denies by default; explicit grants only
--   Law #6 (Tenant Isolation) — all tables: tenant_id + suite_id, FORCE RLS
--   Law #9 (Security)         — inbox dedup via idempotency_key; dead-letter on failure
--
-- FK notes (critical):
--   approval_requests.approval_id is TEXT PRIMARY KEY (trust_spine_bundle schema)
--   receipts.receipt_id is TEXT PRIMARY KEY (trust_spine_bundle schema)
--   threads.thread_id is UUID (migration 095)
--   memory_objects.memory_id is UUID (migration 096)
--
-- References:
--   02_SHARED_SCHEMAS.md §2.2, §2.5, §2.6, §2.7
--   03_THREAD_REGISTRY_AND_MEMORY_MODEL.md §3.5 (latest brief caches)
-- =============================================================================

-- =============================================================================
-- TABLE: public.proactive_candidates
-- =============================================================================
-- Surface-level action suggestions generated by agents during session processing.
-- The orchestrator reads these to decide whether to route, draft, or request approval.
-- Dedup key: one open/snoozed candidate per (tenant, suite, office, owner_agent,
--            entity, recommended_action) at a time.

CREATE TABLE IF NOT EXISTS public.proactive_candidates (
    -- Primary key
    candidate_id    UUID    PRIMARY KEY DEFAULT gen_random_uuid(),

    -- Schema versioning
    schema_version  TEXT    NOT NULL DEFAULT 'v1',

    -- Tenant / suite / office scope (Law #6)
    tenant_id       UUID    NOT NULL,
    suite_id        UUID    NOT NULL,
    office_id       UUID    NOT NULL,

    -- Owning agent
    owner_agent     TEXT    NOT NULL CHECK (owner_agent IN (
                                'ava', 'sarah', 'eli', 'nora', 'finn', 'tim', 'system'
                            )),

    -- Source event / memory references
    source_event_ids    UUID[]  NOT NULL DEFAULT '{}',
    source_memory_ids   UUID[]  NOT NULL DEFAULT '{}',

    -- Entity / thread linkage
    entity_type     TEXT    NULL,
    entity_id       UUID    NULL,
    thread_id       UUID    NULL REFERENCES public.threads(thread_id) ON DELETE SET NULL,

    -- Recommendation
    recommended_action  TEXT    NOT NULL CHECK (recommended_action IN (
                                    'create_draft',
                                    'queue_callback',
                                    'request_approval',
                                    'route_to_agent',
                                    'queue_workflow_trigger',
                                    'surface_warning',
                                    'create_internal_task',
                                    'schedule_outbound_voice',
                                    'none'
                                )),
    action_class        TEXT    NOT NULL CHECK (action_class IN (
                                    'internal_only',
                                    'draft',
                                    'approval_request',
                                    'outbound',
                                    'workflow'
                                )),

    -- Reasoning
    why_now         TEXT    NOT NULL,
    confidence      FLOAT   NOT NULL CHECK (confidence >= 0 AND confidence <= 1),

    -- Governance classification
    risk_tier       TEXT    NOT NULL CHECK (risk_tier IN ('green', 'yellow', 'red')),
    needs_approval  BOOLEAN NOT NULL DEFAULT false,
    receipt_required BOOLEAN NOT NULL DEFAULT false,

    -- Scheduling
    due_at          TIMESTAMPTZ NULL,
    cooldown_until  TIMESTAMPTZ NULL,

    -- Lifecycle status
    status          TEXT    NOT NULL DEFAULT 'open'
                        CHECK (status IN (
                            'open', 'snoozed', 'approved', 'executed', 'dismissed', 'expired'
                        )),

    -- Timestamps
    created_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    last_activity_at    TIMESTAMPTZ NOT NULL DEFAULT now()
);

COMMENT ON TABLE public.proactive_candidates IS
    'Surface-level action suggestions generated by agents. '
    'Orchestrator consumes these to route, draft, or request approvals. '
    'Dedup: one open/snoozed row per (tenant, suite, office, owner_agent, entity, action). '
    'RLS: tenant_id + suite_id + office_id scoped (Law #6).';

-- Dedup: only one active recommendation per context (prevents duplicate nudges)
CREATE UNIQUE INDEX IF NOT EXISTS uq_proactive_candidates_active
    ON public.proactive_candidates (
        tenant_id, suite_id, office_id, owner_agent,
        entity_type, entity_id, recommended_action
    )
    WHERE status IN ('open', 'snoozed');

-- Workqueue scan: pending candidates ordered by due_at
CREATE INDEX IF NOT EXISTS idx_proactive_candidates_queue
    ON public.proactive_candidates (tenant_id, suite_id, status, due_at ASC NULLS LAST)
    WHERE status IN ('open', 'snoozed');

-- Thread linkage
CREATE INDEX IF NOT EXISTS idx_proactive_candidates_thread
    ON public.proactive_candidates (thread_id, last_activity_at DESC)
    WHERE thread_id IS NOT NULL;

-- Touch trigger (maintain last_activity_at)
CREATE OR REPLACE FUNCTION public.trg_proactive_candidates_touch()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
    NEW.last_activity_at := now();
    RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_proactive_candidates_touch ON public.proactive_candidates;
CREATE TRIGGER trg_proactive_candidates_touch
    BEFORE INSERT OR UPDATE ON public.proactive_candidates
    FOR EACH ROW EXECUTE FUNCTION public.trg_proactive_candidates_touch();

-- RLS
ALTER TABLE public.proactive_candidates ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.proactive_candidates FORCE ROW LEVEL SECURITY;

CREATE POLICY proactive_candidates_select_tenant ON public.proactive_candidates
    FOR SELECT TO authenticated
    USING (app.is_member(tenant_id::text));

CREATE POLICY proactive_candidates_all_service_role ON public.proactive_candidates
    FOR ALL TO service_role
    USING (true) WITH CHECK (true);

CREATE POLICY proactive_candidates_insert_tenant ON public.proactive_candidates
    FOR INSERT TO authenticated
    WITH CHECK (
        tenant_id::text IN (
            SELECT tm.tenant_id FROM public.tenant_memberships tm
            WHERE tm.user_id = auth.uid()
        )
        AND suite_id = public.current_suite_id()
    );

CREATE POLICY proactive_candidates_update_tenant ON public.proactive_candidates
    FOR UPDATE TO authenticated
    USING (app.is_member(tenant_id::text))
    WITH CHECK (
        tenant_id::text IN (
            SELECT tm.tenant_id FROM public.tenant_memberships tm
            WHERE tm.user_id = auth.uid()
        )
    );

GRANT SELECT, INSERT, UPDATE ON public.proactive_candidates TO authenticated;
GRANT ALL ON public.proactive_candidates TO service_role;


-- =============================================================================
-- TABLE: public.approval_links
-- =============================================================================
-- Joins approval_requests (trust_spine_bundle TEXT PK) to memory objects
-- and proactive candidates. One row per approval — UNIQUE (approval_id).

CREATE TABLE IF NOT EXISTS public.approval_links (
    approval_link_id    UUID    PRIMARY KEY DEFAULT gen_random_uuid(),

    -- Tenant / suite scope (Law #6) — mirrors approval_requests tenant scoping
    tenant_id           UUID    NOT NULL,
    suite_id            UUID    NOT NULL,

    -- FK to approval_requests (TEXT PK per trust_spine_bundle)
    approval_id         TEXT    NOT NULL
                            REFERENCES public.approval_requests(approval_id)
                            ON DELETE CASCADE,

    -- Linked candidate (optional — approval may be system-initiated)
    linked_candidate_id UUID    NULL
                            REFERENCES public.proactive_candidates(candidate_id)
                            ON DELETE SET NULL,

    -- Memory objects that provided context for this approval
    linked_memory_ids   UUID[]  NOT NULL DEFAULT '{}',

    -- Workflow run that this approval gates (optional)
    linked_workflow_run_id  UUID    NULL,

    -- Requesting agent
    requested_by_agent  TEXT    NOT NULL CHECK (requested_by_agent IN (
                                    'ava', 'sarah', 'eli', 'nora', 'finn', 'tim', 'system'
                                )),

    -- Approval outcome fields (denormalized for fast agent reads)
    approval_status     TEXT    NOT NULL DEFAULT 'pending'
                            CHECK (approval_status IN (
                                'pending', 'approved', 'rejected', 'expired'
                            )),

    requested_at        TIMESTAMPTZ NOT NULL DEFAULT now(),
    decided_at          TIMESTAMPTZ NULL,
    approver_actor_id   UUID        NULL,
    reason              TEXT        NULL,

    -- Audit
    created_at          TIMESTAMPTZ NOT NULL DEFAULT now(),

    -- One link per approval request
    CONSTRAINT uq_approval_links_approval_id UNIQUE (approval_id)
);

COMMENT ON TABLE public.approval_links IS
    'Joins approval_requests (TEXT PK) to memory objects and proactive candidates. '
    'One row per approval — UNIQUE (approval_id). '
    'RLS: tenant_id + suite_id scoped (Law #6).';

CREATE INDEX IF NOT EXISTS idx_approval_links_tenant
