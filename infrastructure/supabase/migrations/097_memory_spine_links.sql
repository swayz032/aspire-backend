-- =============================================================================
-- TRIGGER: memory_objects AFTER INSERT → update threads cache
-- =============================================================================
-- When a new memory_object is inserted and it references a thread,
-- update threads.latest_memory_id and threads.last_activity_at.
-- Uses service_role security context (SECURITY DEFINER) to bypass RLS on threads
-- since the trigger fires in the context of the inserting role.

CREATE OR REPLACE FUNCTION public.trg_memory_objects_update_thread_cache()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
    -- Only update when the memory object is linked to a thread
    IF NEW.thread_id IS NOT NULL THEN
        UPDATE public.threads
        SET
            latest_memory_id = NEW.memory_id,
            last_activity_at = now()
        WHERE
            thread_id = NEW.thread_id
            -- Tenant safety: only update threads within the same tenant+suite
            AND tenant_id = NEW.tenant_id
            AND suite_id  = NEW.suite_id;
    END IF;

    RETURN NULL; -- AFTER trigger; return value ignored
END;
$$;

COMMENT ON FUNCTION public.trg_memory_objects_update_thread_cache() IS
    'Maintains threads.latest_memory_id and threads.last_activity_at cache. '
    'Fires AFTER INSERT on memory_objects. SECURITY DEFINER for cross-table write. '
    'Tenant safety: only updates threads within the same (tenant_id, suite_id).';

DROP TRIGGER IF EXISTS trg_memory_objects_update_thread_cache ON public.memory_objects;
CREATE TRIGGER trg_memory_objects_update_thread_cache
    AFTER INSERT ON public.memory_objects
    FOR EACH ROW EXECUTE FUNCTION public.trg_memory_objects_update_thread_cache();


-- =============================================================================
-- pg_cron: 30-day TTL pruning for processed memory_event_inbox rows
-- =============================================================================
-- Prune processed events older than 30 days. Dead-lettered events are preserved.
-- Wrapped in a DO block so it degrades gracefully if pg_cron is not available.

DO $$
BEGIN
    IF EXISTS (
        SELECT 1 FROM pg_extension WHERE extname = 'pg_cron'
    ) THEN
        PERFORM cron.schedule(
            'prune_memory_event_inbox_ttl',
            '0 3 * * *',    -- daily at 03:00 UTC
            $cron$
                DELETE FROM public.memory_event_inbox
                WHERE status = 'processed'
                  AND created_at < now() - INTERVAL '30 days';
            $cron$
        );
        RAISE NOTICE 'pg_cron job ''prune_memory_event_inbox_ttl'' scheduled.';
    ELSE
        RAISE NOTICE 'pg_cron not available — memory_event_inbox TTL pruning must be run manually.';
    END IF;
END $$;
